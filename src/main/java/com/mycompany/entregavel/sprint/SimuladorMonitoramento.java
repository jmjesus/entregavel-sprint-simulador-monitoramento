    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        Integer porcentagemCPU;
        Integer porcentagemMemoria;
        Integer porcentagemDisco;
        
        Random aleatorio = new Random();
        
        porcentagemCPU = aleatorio.nextInt(101);
        String porcentagemCPUString = porcentagemCPU.toString();
        
        porcentagemMemoria = aleatorio.nextInt(101);
        String porcentagemMemoriaString = porcentagemMemoria.toString();
        
        porcentagemDisco = aleatorio.nextInt(101);
        String porcentagemDiscoString = porcentagemDisco.toString();
        
        lblPorcentagemCPU.setText(porcentagemCPUString + "%");
        lblPorcentagemMemoria.setText(porcentagemMemoriaString + "%");
        lblPorcentagemDisco.setText(porcentagemDiscoString + "%");
        
        if(porcentagemCPU > 75){
            prgCPU.setForeground(Color.RED);
        } else if(porcentagemCPU > 45){
            prgCPU.setForeground(Color.ORANGE);
        } else {
            prgCPU.setForeground(Color.GREEN);
        }
        
        if(porcentagemMemoria > 75){
            prgMemoria.setForeground(Color.RED);
        } else if(porcentagemMemoria > 45){
            prgMemoria.setForeground(Color.ORANGE);
        } else {
            prgMemoria.setForeground(Color.GREEN);
        }
        
        if(porcentagemDisco > 75){
            prgDisco.setForeground(Color.RED);
        } else if(porcentagemDisco > 45){
            prgDisco.setForeground(Color.ORANGE);
        } else {
            prgDisco.setForeground(Color.GREEN);
        }
        
        prgCPU.setValue(porcentagemCPU);
        prgMemoria.setValue(porcentagemMemoria);
        prgDisco.setValue(porcentagemDisco);
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

